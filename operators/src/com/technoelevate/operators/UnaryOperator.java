package com.technoelevate.operators;

public class UnaryOperator {

	public static void main(String[] args) {
	int y=19;
	int x=-2;
	System.out.println(~x );
	System.out.println(~~x);
	System.out.println(~~~x);
	System.out.println(~y);
	System.out.println(~~y);
	System.out.println(~~~y);
	
	

	}

}
